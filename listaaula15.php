a média geral. Caso a média seja maior ou igual a seis, exiba aprovado, 
caso contrário, exiba reprovado. Exiba também a média final calculada. 
Ex: N1 = 5 | N2 = 10 | N3 = 4 | MG = 6,33 [Aprovado] 
